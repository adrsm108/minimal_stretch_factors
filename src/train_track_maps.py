def int_tuple_cmp(a: int | tuple[int, ...], b: int | tuple[int, ...]) -> int:
    if isinstance(a, tuple):
        if isinstance(b, tuple):
            if a == b:
                return 0
            elif a < b:
                return -1
            else:
                return 1
        else:
            return 1
    else:
        if isinstance(b, tuple):
            return -1
        else:
            return a - b


