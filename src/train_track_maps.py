def stack_info(tg: DiGraph, canonical=False, indices=False, graph=False) -> StackInfo:
    r"""
    List the stacks and images of final edges for a transition digraph

    Given a digraph ``tg`` representing the transition matrix of a graph map
    :math:`f : G \to G`, a stack is an equivalence class of edges in :math:`G` (vertices in ``tg``)
    generated by the relation :math:`e \sim f(e)` if :math:`e` is neither mixing nor surplus
    (equivalently, if `e` has out-degree 1 as a vertex of ``tg``, and this neighbor has in-degree 1).
    Stacks are returned as lists in mapping order, ``[e, f(e), f(f(e)), ...]``.

    .. WARNING::

    Algorithm assumes but does not check that ``tg`` is strongly connected.

    INPUT:

    - ``tg`` -- DiGraph, the strongly connected transition digraph of an irreducible graph map ``f``.

    OUTPUT: a tuple of

    - list of stacks partitioning the vertices of ``tg``
    - list of pairs ``(v, out_neighbors)``, one for each vertex of
      ``tg`` with outdegree > 1. Vertices in ``out_neighbors`` are listed with multiplicity.

    """

    stacks = list[list]()
    stack_index = dict[int, int]()
    images = list[tuple[int, tuple[int]]]()
    queue = deque[int]()

    def get_index(v, start=None):
        if v not in stack_index:
            if start is None:
                start = v
            ws = tg.neighbors_out(v)
            if tg.out_degree(v) == 1 and tg.in_degree(ws[0]) == 1 and ws[0] != start:
                # really v should be inserted in front, but that's expensive
                # instead we append and then reverse all stacks later
                stacks[stack_index.setdefault(v, get_index(ws[0], start))].append(v)
            else:
                stack_index[v] = len(stacks)
                stacks.append([v])
                images.append((v, tuple(w for w in ws for _ in tg.edge_label(v, w))))
                queue.extend(w for w in ws if w not in stack_index)

        return stack_index[v]

    if len(tg) > 0:
        queue.append(first(tg))

    while queue:
        get_index(queue.popleft())

    for stack in stacks:
        stack.reverse()

    result = StackInfo(stacks=stacks, images=images)
    if indices:
        result["indices"] = stack_index

    if not (canonical or graph):
        return result

    sg = stack_graph(stacks, images, stack_index, canonicalize=canonical)

    if graph:
        result["graph"] = sg

    return result


def lsbi(n):
    """Return index of lowest set bit, or -1 if `n` is 0"""
    return (n & -n).bit_length() - 1


def changeable_run_positions(A: Iterable, preserve):
    if not preserve:
        yield from run_positions(A)
        return

    preserve = {x: True for x in preserve}
    iterator = iter(A)
    start = i = 0
    try:
        x = next(iterator)
    except StopIteration:
        return

    for y in iterator:
        i += 1
        if x != y:
            if x in preserve and preserve[x]:
                preserve[x] = False
            else:
                yield start, i
            start = i
        x = y

    if not (x in preserve and preserve[x]):
        yield start, i + 1


def valid_paths(
    edges: Iterable[int],
    signed=True,
    /,
    positive=None,
    forward: dict[int, int] | bool | None = None,
):
    r"""
    Generate all nondegenerate paths over a collection of edges

    A nondegenerate path is a sequence of edges with no subsequences of the form :math:`e, e^{-1}`.
    Edges are encoded as nonzero integers with sign indicating orientation:
    `-n` is treated as `n` traversed in reverse.
    A collection of edges which should initially be traversed positively.
    For every edge `e` in ``preserve``, we only generate paths which the first occurence of `e` has positive sign.

    Input:

    - ``edges`` -- collection of positive integers. Elements need not be distinct.

    - ``positive`` -- collection of positive integers representing the edges that should
      have a positive orientation the first time they appear in a generated path

    Output: a generator yielding paths over ``edges`` as tuples of length ``len(edges)``.
    """
    A = list(abs(e) for e in edges)
    if not A:
        return
    A.sort()
    n = len(A)

    if not forward:
        is_acceptable = lambda: True
    elif isinstance(forward, dict):
        is_acceptable = lambda: (
            first_truthy(forward[A[i]] - forward[A[n - i - 1]] for i in range(n // 2))
            <= 0
        )
    else:
        is_acceptable = lambda: (
            first_truthy(A[i] - A[n - i - 1] for i in range(n // 2)) <= 0
        )

    while True:
        # A holds a permutation of the positive edges
        if (
            is_acceptable()
        ):  # if the permutation is not a reversal of one that came before it
            # yield all valid combinations of signs
            # we change signs in-place and end up back where we started (all entries positive)
            if signed and (
                runs := list(changeable_run_positions(A, positive))
            ):  # runs of identical edges must have the same sign
                for i in range(1 << len(runs)):
                    yield tuple(A)
                    # gray[i] ^ gray[i-1] == 1 << lowest_set_bit_index(i)
                    for j in range(*runs[lsbi(i)]):
                        A[j] = -A[j]
            else:
                yield tuple(A)

        # All entries of A are once again positive. generate the next permutation
        # https://en.wikipedia.org/wiki/Permutation#Generation_in_lexicographic_order
        for i in range(n - 2, -1, -1):
            if A[i] < A[i + 1]:
                break
        else:
            return

        for j in range(n - 1, i, -1):
            if A[i] < A[j]:
                break

        A[i], A[j] = A[j], A[i]
        A[i + 1 :] = A[: i - n : -1]  # A[i + 1:][::-1]


def degree_sequence(vert_partition: DisjointSet) -> DegreeSeq:
    """Get the degree sequence associated with a partition of edge germs."""
    degs = {}
    for subset in vert_partition:
        n = len(subset)
        if n not in degs:
            degs[n] = 1
        else:
            degs[n] += 1

    return tuple(
        sorted(degs.items(), reverse=True)
    )  # (tuple((k, degs[k]) for k in sorted(degs, reverse=True)))


def max_trivalent(degs: dict[int, int]):
    r"""
    Return the maximum number of valence-3 verts among all graphs that can be
    obtained from a graph with the given degree sequence by identifying vertices.
    """
    ones = degs.get(1, 0)
    m = min(degs.get(2, 0), ones)
    return degs.get(3, 0) + m + (ones - m) // 3


@cache
def _integer_partitions_poset(n: int):
    """Return a cached instance of posets.IntegerPartitions(n)"""
    return posets.IntegerPartitions(n)


@cache
def _is_admissible_degree_sequence(degs: DegreeSeq, rank: int) -> bool:
    r"""
    Check whether a degree sequence is admissible in the given rank.

    We say a degree sequence is admissible in rank `r` if it can be
    realized by a graph `G` whose edge germ partition is admissible in rank `r`.
    Equivalently, if the vertices of `G` can be identified to produce
    a connected graph of rank `r` with no valence :math:`\le 2` vertices.

    Inputs:

    - ``degs`` -- degree sequence, represented as a run-length encoded tuple of
      integer pairs :math:`(d_k, n_k), \dots, (d_1, n_1)`, where :math:`d_k > \dotsc > d_1`.

    - ``rank`` -- positive integer

    Outputs: ``True`` if ``degs`` is admissible in the given rank
    """
    N, n_verts, n_edges = cell_counts(degs, rank)

    # first run a simple check based on number of trivalent vertices
    min_trivalent = max(
        0, n_verts - (N - 3 * n_verts)
    )  # min number of deg-3 verts req'd for this graph to have no valence <= 2 verts
    if max_trivalent(dict(degs)) < min_trivalent:
        return False

    # no more quick checks, we need to verify
    deg_part = tuple(from_rle(degs))
    poset = _integer_partitions_poset(N)

    # equivalent to Partitions(N, length=n_verts, min_part=3)
    for part in Partitions_length_and_parts_constrained(N, n_verts, n_verts, 3, N):
        if poset.is_gequal(deg_part, tuple(part)):  # deg_part is a refinement of part
            return True
    else:
        return False


def is_admissible_partition(germs: DisjointSet, rank: int) -> bool:
    r"""
    Check whether a partition of edge germs is admissible in the given rank.

    We say a disjoint union of edge germs `P` is admissible in rank `r`
    if there exists a connected graph `G` of rank `r` such that:

    - `G` has no vertices of degree :math:`\le 2`, and
    - `P` is a refinement of the partition on edge germs given by the vertices of `G`.

    Inputs:

    - ``germs`` -- disjoint set of edge germs

    - ``rank`` -- positive integer, the rank of `G`

    Outputs: ``True`` if ``germs`` is admissible in the given rank.
    """
    return _is_admissible_degree_sequence(degree_sequence(germs), rank)


def depth_first_neighbors(g: DiGraph, start):
    neighbors = {v: set() for v in g}
    for u, v in g.depth_first_search(start, edges=True):
        neighbors[u].add(v)
    return neighbors


def candiate_train_track_maps(tg: DiGraph, rank: int, canonical=True):
    """
    Get all candidates for train-track maps with a given transition digraph and domain rank

    Inputs:

    - ``tg`` -- digraph with positive integer vertices. Should be strongly connected.
    - ``rank`` -- positive integer

    Outputs: a list of pairs ``(f, pf)``

    - ``f`` -- ``PartialGraphMap`` with transition digraph ``tg``. Returned maps are
      guaranteed to be train-track and complete (no edge images left unspecified).
    - ``pf`` -- disjoint set of integers representing the partition induced by ``f`` on edge germs of its domain.

    Returns ``[]`` if no such maps exist.
    """
    n_edges = len(tg)
    n_verts = n_edges - rank + 1

    info = stack_info(tg, canonical=canonical, indices=canonical, graph=canonical)
    stacks = info["stacks"]
    edge_mappings = info["images"]
    candidate_maps = list[tuple[PartialGraphMap, DisjointSet]]()

    if canonical:
        s = info["indices"]
        sg = info["graph"]
        o = depth_first_neighbors(sg, 0)
        positive = {
            e: tuple(img_e for img_e in dedupe_adjacent(img) if s[img_e] in o[s[e]])
            for e, img in edge_mappings
        }
        forward = {
            first_truthy(
                e
                for e, img in edge_mappings
                if len(img) > 1 and s[img[0]] != s[img[-1]]
            ): s
        }
    else:
        positive = {}
        forward = {}

    def go(f: PartialGraphMap, germs: DisjointSet, remaining: list[tuple]):
        if len(remaining) == 0:
            if is_admissible_partition(germs, rank):
                candidate_maps.append((f, germs))
            return

        (e, image_edges), *remaining = remaining

        for fe in valid_paths(
            image_edges,
            positive=positive.get(e, None),
            forward=forward.get(e, None),
        ):
            new_f = f.new_edge_mapping(e, fe)
            try:
                new_verts = new_f.identify_vertices(
                    germs,
                    assert_train_track=True,
                    min_subsets=n_verts - 1,
                )
                if new_verts.number_of_subsets() >= n_verts:
                    go(new_f, new_verts, remaining)
            except AssertionError:  # exception raised if new_f is not train-track
                continue

    go(
        PartialGraphMap.from_stacks(stacks),
        DisjointSet(i for i in range(-n_edges, n_edges + 1) if i),
        edge_mappings,
    )

    return candidate_maps


def cell_counts(degs: DegreeSeq | DegreeDict, rank: int):
    N = sum(
        d * n for d, n in (degs.items() if isinstance(degs, dict) else degs)
    )  # sum of vertex degrees

    # number of verts and edges in a conn. graph with the given rank and degree seq
    if N % 2 == 0:
        n_edges = N // 2
    else:  # sequence is not graphical
        raise ValueError("Sum of vertex degrees must be even")
    n_verts = n_edges - rank + 1

    return N, n_verts, n_edges


def train_track_domains(f: PartialGraphMap, germs: DisjointSet, rank):
    degs = degree_sequence(germs)
    N, n_verts, n_edges = cell_counts(degs, rank)

    # no more quick checks, we need to verify
    deg_partition = tuple(from_rle(degs))

    poset = _integer_partitions_poset(N)

    # equivalent to Partitions(N, length=n_verts, min_part=3)
    for part in Partitions_length_and_parts_constrained(N, n_verts, n_verts, 3, N):
        if poset.is_gequal(
            deg_partition, tuple(part)
        ):  # deg_part is a refinement of part
            return True
    else:
        return False


def train_track_maps(tg: DiGraph, rank: int):
    n_edges = len(tg)
    N = 2 * n_edges
    n_verts = n_edges - rank + 1
    if hasattr(tg, "candidate_maps"):
        candidate_maps = tg.refinement
    else:
        candidate_maps = candiate_train_track_maps(tg, rank)
        tg.candidate_maps = candidate_maps

    for f, germs in candidate_maps:
        degs = degree_sequence(germs)
        deg_part = from_rle(degs)
        poset = _integer_partitions_poset(N)

        # equivalent to Partitions(N, length=n_verts, min_part=3)
        for part in Partitions_length_and_parts_constrained(N, n_verts, n_verts, 3, N):
            if poset.is_gequal(
                deg_part, tuple(part)
            ):  # deg_part is a refinement of part
                return True


